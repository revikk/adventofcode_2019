-module(day5_tests).

-include_lib("eunit/include/eunit.hrl").

intcode_test_() ->
    [?_assertEqual([3500, 9, 10, 70, 2, 3, 11, 0, 99, 30, 40, 50],
                   day5:intcode([1, 9, 10, 3, 2, 3, 11, 0, 99, 30, 40, 50])),
     ?_assertEqual([2, 0, 0, 0, 99], day5:intcode([1, 0, 0, 0, 99])),
     ?_assertEqual([2, 3, 0, 6, 99], day5:intcode([2, 3, 0, 3, 99])),
     ?_assertEqual([2, 4, 4, 5, 99, 9801], day5:intcode([2, 4, 4, 5, 99, 0])),
     ?_assertEqual([30, 1, 1, 4, 2, 5, 6, 0, 99], day5:intcode([1, 1, 1, 4, 99, 5, 6, 0, 99])),
     ?_assertEqual(1, day5:intcode(8, [3, 9, 8, 9, 10, 9, 4, 9, 99, -1, 8])),
     ?_assertEqual(0, day5:intcode(7, [3, 9, 8, 9, 10, 9, 4, 9, 99, -1, 8])),
     ?_assertEqual(0, day5:intcode(9, [3, 9, 8, 9, 10, 9, 4, 9, 99, -1, 8])),
     ?_assertEqual(0, day5:intcode(9, [3, 9, 7, 9, 10, 9, 4, 9, 99, -1, 8])),
     ?_assertEqual(0, day5:intcode(8, [3, 9, 7, 9, 10, 9, 4, 9, 99, -1, 8])),
     ?_assertEqual(1, day5:intcode(7, [3, 9, 7, 9, 10, 9, 4, 9, 99, -1, 8])),
     ?_assertEqual(0, day5:intcode(7, [3, 3, 1108, -1, 8, 3, 4, 3, 99])),
     ?_assertEqual(1, day5:intcode(8, [3, 3, 1108, -1, 8, 3, 4, 3, 99])),
     ?_assertEqual(0, day5:intcode(9, [3, 3, 1108, -1, 8, 3, 4, 3, 99])),
     ?_assertEqual(0, day5:intcode(9, [3, 3, 1107, -1, 8, 3, 4, 3, 99])),
     ?_assertEqual(0, day5:intcode(8, [3, 3, 1107, -1, 8, 3, 4, 3, 99])),
     ?_assertEqual(1, day5:intcode(7, [3, 3, 1107, -1, 8, 3, 4, 3, 99])),
     ?_assertEqual(0,
                   day5:intcode(0, [3, 12, 6, 12, 15, 1, 13, 14, 13, 4, 13, 99, -1, 0, 1, 9])),
     ?_assertEqual(1,
                   day5:intcode(1, [3, 12, 6, 12, 15, 1, 13, 14, 13, 4, 13, 99, -1, 0, 1, 9])),
     ?_assertEqual(0, day5:intcode(0, [3, 3, 1105, -1, 9, 1101, 0, 0, 12, 4, 12, 99, 1])),
     ?_assertEqual(1, day5:intcode(1, [3, 3, 1105, -1, 9, 1101, 0, 0, 12, 4, 12, 99, 1])),
     ?_assertEqual(999,
                   day5:intcode(7,
                                [3, 21, 1008, 21, 8, 20, 1005, 20, 22, 107, 8, 21, 20, 1006, 20, 31,
                                 1106, 0, 36, 98, 0, 0, 1002, 21, 125, 20, 4, 20, 1105, 1, 46, 104,
                                 999, 1105, 1, 46, 1101, 1000, 1, 20, 4, 20, 1105, 1, 46, 98, 99])),
     ?_assertEqual(1000,
                   day5:intcode(8,
                                [3, 21, 1008, 21, 8, 20, 1005, 20, 22, 107, 8, 21, 20, 1006, 20, 31,
                                 1106, 0, 36, 98, 0, 0, 1002, 21, 125, 20, 4, 20, 1105, 1, 46, 104,
                                 999, 1105, 1, 46, 1101, 1000, 1, 20, 4, 20, 1105, 1, 46, 98, 99])),
     ?_assertEqual(1001,
                   day5:intcode(9,
                                [3, 21, 1008, 21, 8, 20, 1005, 20, 22, 107, 8, 21, 20, 1006, 20, 31,
                                 1106, 0, 36, 98, 0, 0, 1002, 21, 125, 20, 4, 20, 1105, 1, 46, 104,
                                 999, 1105, 1, 46, 1101, 1000, 1, 20, 4, 20, 1105, 1, 46, 98,
                                 99]))].
